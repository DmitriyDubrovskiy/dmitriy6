using System;
using System.Collections.Generic;

class Дорога
{
    public double Довжина { get; set; }
    public double Ширина { get; set; }
    public int КількістьСмуг { get; set; }
    public double РівеньТрафіку { get; set; }
}

class ТранспортнийЗасіб : IDriveable
{
    public double Швидкість { get; set; }
    public double Розмір { get; set; }
    public string Тип { get; set; }

    public void Рухатися()
    {
        Console.WriteLine($"Транспортний засіб типу {Тип} рухається зі швидкістю {Швидкість} км/год.");
    }

    public void Зупинитися()
    {
        Console.WriteLine($"Транспортний засіб типу {Тип} зупинився.");
    }
}

interface IDriveable
{
    void Рухатися();
    void Зупинитися();
}

class Місто
{
    private List<Дорога> дороги;
    private List<ТранспортнийЗасіб> транспорт;

    public Місто()
    {
        дороги = new List<Дорога>();
        транспорт = new List<ТранспортнийЗасіб>();
    }

    public void ДодатиДорогу(Дорога дорога)
    {
        дороги.Add(дорога);
    }

    public void ДодатиТранспорт(ТранспортнийЗасіб транспортнийЗасіб)
    {
        транспорт.Add(транспортнийЗасіб);
    }

    public void МоделюватиРух()
    {
        foreach (ТранспортнийЗасіб транспортнийЗасіб in транспорт)
        {
            транспортнийЗасіб.Рухатися();
        }
    }
}

class Program
{
    static void Main()
    {
        Місто місто = new Місто();

        Дорога дорога1 = new Дорога { Довжина = 10, Ширина = 3, КількістьСмуг = 2, РівеньТрафіку = 50 };
        Дорога дорога2 = new Дорога { Довжина = 5, Ширина = 2, КількістьСмуг = 1, РівеньТрафіку = 30 };

        ТранспортнийЗасіб автомобіль = new ТранспортнийЗасіб { Швидкість = 60, Розмір = 4, Тип = "автомобіль" };
        ТранспортнийЗасіб автобус = new ТранспортнийЗасіб { Швидкість = 40, Розмір = 6, Тип = "автобус" };

        місто.ДодатиДорогу(дорога1);
        місто.ДодатиДорогу(дорога2);
        місто.ДодатиТранспорт(автомобіль);
        місто.ДодатиТранспорт(автобус);

        місто.МоделюватиРух();
    }
}
